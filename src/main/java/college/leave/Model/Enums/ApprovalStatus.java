package college.leave.Model.Enums;

/**
 * Represents the approval status of a leave request.
 * This enum defines the possible states of a leave application.
 */
public enum ApprovalStatus {
    /**
     * Indicates that the leave request has been approved.
     */
    APPROVED,

    /**
     * Indicates that the leave request is pending review.
     */
    PENDING,

    /**
     * Indicates that the leave request has been rejected.
     */
    REJECTED
}
